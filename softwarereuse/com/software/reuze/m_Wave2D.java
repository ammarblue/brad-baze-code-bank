public class m_Wave2D {

    public m_a_Wave xmod, ymod;
    public ga_Vector2 pos;

    public m_Wave2D(m_a_Wave x, m_a_Wave y) {
        xmod = x;
        ymod = y;
        pos = new ga_Vector2();
    }

    public void update() {
        pos.x = xmod.update();
        pos.y = ymod.update();
    }
}

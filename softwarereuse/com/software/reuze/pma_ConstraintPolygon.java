public class pma_ConstraintPolygon implements pma_i_ConstraintParticle {

    protected ga_Polygon poly;
    private boolean isContainer;

    public pma_ConstraintPolygon(ga_Polygon poly, boolean isContainer) {
        this.poly = poly;
        this.isContainer = isContainer;
    }

    public void apply(pma_ParticleVerlet p) {
        if (poly.contains(p) != isContainer) {
            poly.constrain(p);
        }
    }

    public ga_Polygon getPolygon() {
        return poly;
    }

    public boolean isContainer() {
        return isContainer;
    }

    public void setIsContainer(boolean isContainer) {
        this.isContainer = isContainer;
    }

    public void setPolygon(ga_Polygon poly) {
        this.poly = poly;
    }
    
    public ArrayList<ga_Vector2> getPoints() {
		return poly.getPoints();
	}
}
